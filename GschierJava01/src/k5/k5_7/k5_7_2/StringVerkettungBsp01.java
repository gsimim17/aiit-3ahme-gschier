package k5;

// Listing 5.3 - 5.5

public class StringVerkettungBsp01 {
    public void stringVerkettungBsp01() {
        int a = 3;
        int b = 4;
        
        // Listing 5.3
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        
        // Listing 5.4
        System.out.println("a + b = " + a + b);
        
        // Listing 5.5
        System.out.println("a + b = " + (a + b));
    }
}
